# Entrada
print("Este programa calcula o valor gasto com passagem de ônibus de um grupo de 20 pessoas, sendo que o preço unitário da passagem é de R$ 7,30")
# A quantidade de pessoas é 20
# O valor unitário da passagem é R$ 7,30

passageiros = 20

valor_unitario = 7.30


# Processamento
gasto = passageiros * valor_unitario



# Saída
print(f"\nA quantidade de passageiros é {passageiros} .")
print(f"\nO valor unitário da passagem é {valor_unitario} .")
print(f"\nO gasto total com passagens é de R$ {gasto} .")

